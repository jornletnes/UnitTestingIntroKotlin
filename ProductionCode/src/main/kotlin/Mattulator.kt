class Mattulator {
    fun Adder(tall1: Double, tall2: Double): Double {
        return tall1 + tall2
    }

    fun Subtraher(tall1: Double, tall2: Double): Double {
        return tall1 - tall2
    }

    fun Multipliser(tall1: Double, tall2: Double): Double {
        return tall1 * tall2
    }

    fun Divider(teller: Double, nevner: Double): Double {
        return nevner / teller
    }
}